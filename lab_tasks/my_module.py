import math

def sum(a, b):
    return a+b

def difference(a, b):
    return a-b

def multiplication(a,b):
    return a*b

def division(a, b):
    return a/b

def factorial(a):
    result = 1  
    for i in range(1, a + 1):  
        result *= i  
    return result  

def sqwr(a):
    return math.sqrt(a)